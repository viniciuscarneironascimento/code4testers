# Ao atribuir um valor diretor para uma variável automaticamente ela passa a ter o tipo do dado que foi passado
nome = "Vinicius"
idade = 40
legal = true

# Para conferir o tipo, usamos var.class. Neste exemplo o sistema vai mostrar que as variáveis são do tipo string, integer e boolean respectivamente
puts nome 
puts nome.class
puts idade
puts idade.class
puts legal
puts legal.class